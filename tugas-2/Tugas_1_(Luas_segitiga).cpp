/*1. Diketahui sebuah segitiga memiliki panjang alas = 8 cm dan tinggi = 5 cm.
 Buatlah sebuah program dalam Bahasa C untuk menghitung dan menampilkan luas dari segitiga tersebut!*/
#include <stdio.h>

int main () {
	int pa=8;
	int t=7;
	int lu=(pa*t)/2;
	printf("Panjang alas  : %d cm.\n",pa);
	printf("Tinggi        : %d cm.\n",t);
	printf("Luas segitiga : %d cm^2.",lu);
	return 0;
}
